    private void ActualizarFecha() {
            
            try {
            Date fecha = FECHA.getDate();
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yy");
            FechaProxima = sdf.format(fecha);
                Connection con = BD.getConnection();
                Statement st = con.createStatement();
                ResultSet r = st.executeQuery("update BEVALUACION_DESEMPENO set estado = 1,fecha = '"+FechaProxima+"' where id_evaluacion ="+id_evaluacion);
                r.close();
                st.close();
                con.close();
                next();
            } catch (SQLException error) {
               System.out.print(" ERROR QUE OBTIENE EL ULTIMO ID DE INGRESO  " + error );
            }
            
         //System.out.println("1 = "+FechaProxima+ "2 = "+id_listaempleados+" 3 = "+no_evaluacion+" FACE ="+(face+1));
    }
    
   
    
    
    
    
